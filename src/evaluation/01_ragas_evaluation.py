# DEMO
# ─────────────────────────────────────────────────────────────

def main():
    # ── RAGAS Evaluation ──────────────────────────────────────

    eval_samples = [
        EvalSample(
            question="What is LoRA and how does it reduce training parameters?",
            answer="LoRA adds small trainable matrices A and B to frozen model layers, reducing trainable parameters by over 100x compared to full fine-tuning.",
            contexts=[
                "LoRA (Low-Rank Adaptation) freezes the base model and trains two small matrices A and B where ΔW ≈ B@A. This reduces trainable parameters dramatically.",
                "Fine-tuning large models requires updating billions of parameters. LoRA reduces this to thousands of adapter parameters.",
            ],
            ground_truth="LoRA freezes the base model weights and adds low-rank adapter matrices A and B to each attention layer. The weight update ΔW = B@A has rank r << d, reducing parameters by 100x or more.",
        ),
        EvalSample(
            question="What does PSI > 0.2 indicate?",
            answer="A PSI greater than 0.2 indicates significant distribution drift, suggesting the model should be retrained.",
            contexts=[
                "PSI (Population Stability Index) measures distribution shift. PSI < 0.1: no significant change. PSI 0.1-0.2: moderate change. PSI > 0.2: significant shift — model retraining recommended.",
            ],
            ground_truth="PSI > 0.2 indicates significant distribution shift in the feature data, which typically requires investigating the root cause and retraining the model.",
        ),
    ]

    print("=" * 70)
    print("RAGAS EVALUATION RESULTS")
    print("=" * 70)

    evaluator = RAGASEvaluator(simulate=True)
    results = evaluator.evaluate_dataset(eval_samples)

    for i, (sample, result) in enumerate(zip(eval_samples, results)):
        print(f"\nSample {i+1}: '{sample.question[:60]}...'")
        print(f"  Faithfulness:      {result.faithfulness:.3f}")
        print(f"  Answer Relevancy:  {result.answer_relevancy:.3f}")
        print(f"  Context Precision: {result.context_precision:.3f}")
        print(f"  Context Recall:    {result.context_recall:.3f}")
        print(f"  Overall Score:     {result.overall_score:.3f}")

    aggregated = evaluator.aggregate(results)
    print(f"\nAGGREGATE SCORES (n={len(results)}):")
    for metric, score in aggregated.items():
        print(f"  {metric:25s}: {score:.3f}")

    # ── LLM-as-Judge ─────────────────────────────────────────

    print("\n" + "=" * 70)
    print("LLM-AS-JUDGE EVALUATION")
    print("=" * 70)

    judge = LLMJudge(simulate=True)
    judgment = judge.judge(
        question="What is LoRA?",
        reference="LoRA freezes the base model and trains low-rank adapter matrices A and B, reducing trainable parameters by 100x+.",
        generated="LoRA (Low-Rank Adaptation) adds trainable matrices to frozen layers, greatly reducing the number of parameters that need to be trained.",
    )

    print("\nJudgement result:")
    for k, v in judgment.items():
        print(f"  {k:15s}: {v}")

    print("\n" + "=" * 70)
    print("KEY EVALUATION CONCEPTS:")
    print("  RAGAS   → Automated evaluation of RAG quality (4 core metrics)")
    print("  LLM-Judge → GPT-4 scoring at scale (replaces human evaluators)")
    print("  Faithfulness → % of answer claims supported by context (hallucination check)")
    print("  Context Precision → Are retrieved chunks useful? (retrieval quality)")
    print("  Track scores weekly in MLflow → detect quality degradation over time")
    print("=" * 70)


if __name__ == "__main__":
    main()
