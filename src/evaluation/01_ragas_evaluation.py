"""
============================================================
MODULE 05 — LLM Evaluation: RAGAS & LLM-as-Judge
============================================================

WHAT YOU WILL LEARN:
  - The four core RAGAS metrics and what they each measure
  - How to run RAGAS evaluation on a RAG pipeline
  - What LLM-as-Judge means and how to implement it
  - How the CV claim "improving response quality scores by 35%"
    was measured

INTERVIEW QUESTIONS THIS COVERS:
  Q: What is RAGAS?
  A: Retrieval-Augmented Generation Assessment — a framework
     that automatically evaluates RAG pipelines using 4 metrics:
     Faithfulness, Answer Relevancy, Context Precision, Context Recall.

  Q: What is Faithfulness?
  A: % of claims in the generated answer that are actually
     supported by the retrieved context. Low faithfulness = hallucination.

  Q: What is Answer Relevancy?
  A: How well the answer addresses the actual question asked.
     A long, correct but off-topic answer scores low.

  Q: What is Context Precision?
  A: What fraction of the retrieved chunks were actually useful?
     If you retrieve 5 chunks but only 2 were needed, precision is 0.4.

  Q: What is Context Recall?
  A: Were all the chunks needed to answer the question retrieved?
     If the answer requires 3 facts and you retrieved 2 of them, recall is 0.67.

  Q: What is LLM-as-Judge?
  A: Using a powerful LLM (GPT-4) to evaluate another LLM's output.
     Cheaper than human evaluation, scales easily, consistent scoring.
     Prompt: "On a scale 1-5, does this answer correctly address the question?"
============================================================
"""

import os
import json
from typing import List, Dict, Any
from dataclasses import dataclass

from loguru import logger


# ─────────────────────────────────────────────────────────────
# DATA STRUCTURES
# ─────────────────────────────────────────────────────────────

@dataclass
class EvalSample:
    """One question-answer sample for evaluation."""
    question: str
    answer: str                    # Generated by your RAG system
    contexts: List[str]            # Retrieved chunks given to the LLM
    ground_truth: str              # The correct answer (human-labelled)


@dataclass
class EvalResult:
    """Results for one sample."""
    faithfulness: float            # 0.0 - 1.0: are claims supported by context?
    answer_relevancy: float        # 0.0 - 1.0: does answer address the question?
    context_precision: float       # 0.0 - 1.0: are retrieved chunks relevant?
    context_recall: float          # 0.0 - 1.0: were all needed chunks retrieved?

    @property
    def overall_score(self) -> float:
        return (self.faithfulness + self.answer_relevancy +
                self.context_precision + self.context_recall) / 4


# ─────────────────────────────────────────────────────────────
# RAGAS RUNNER
# ─────────────────────────────────────────────────────────────

class RAGASEvaluator:
    """
    Runs RAGAS evaluation on a dataset of RAG outputs.

    PRODUCTION USAGE:
      from ragas import evaluate
      from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall
      from datasets import Dataset

      dataset = Dataset.from_list([...])  # list of dicts with question, answer, contexts, ground_truth
      result = evaluate(dataset, metrics=[faithfulness, answer_relevancy, ...])
      print(result.to_pandas())

    This class simulates the scores so you can study the logic without an API key.
    """

    def __init__(self, simulate: bool = True):
        self.simulate = simulate

    def evaluate_dataset(self, samples: List[EvalSample]) -> List[EvalResult]:
        """Evaluate a list of RAG outputs."""
        results = []

        for sample in samples:
            if self.simulate:
                result = self._simulated_eval(sample)
            else:
                result = self._ragas_eval(sample)

            results.append(result)

        return results

    def _simulated_eval(self, sample: EvalSample) -> EvalResult:
        """
        Simulate RAGAS scores based on simple heuristics.
        In production, RAGAS calls GPT-4 to compute these.
        """
        # Faithfulness: are key words from the answer in the context?
        context_text = " ".join(sample.contexts).lower()
        answer_words = set(sample.answer.lower().split())
        context_words = set(context_text.split())
        faithfulness = min(1.0, len(answer_words & context_words) / (len(answer_words) + 1))

        # Answer relevancy: word overlap between question and answer
        question_words = set(sample.question.lower().split())
        relevancy = min(1.0, len(answer_words & question_words) / (len(question_words) + 1))
        answer_relevancy = 0.5 + 0.5 * relevancy  # Baseline 0.5

        # Context precision: how many retrieved chunks contain relevant words?
        relevant_chunks = sum(
            1 for ctx in sample.contexts
            if any(w in ctx.lower() for w in question_words)
        )
        context_precision = relevant_chunks / len(sample.contexts) if sample.contexts else 0

        # Context recall: does the context cover the ground truth?
        gt_words = set(sample.ground_truth.lower().split())
        recall = min(1.0, len(gt_words & context_words) / (len(gt_words) + 1))
        context_recall = 0.4 + 0.6 * recall  # Baseline 0.4

        return EvalResult(
            faithfulness=round(faithfulness, 3),
            answer_relevancy=round(answer_relevancy, 3),
            context_precision=round(context_precision, 3),
            context_recall=round(context_recall, 3),
        )

    def _ragas_eval(self, sample: EvalSample) -> EvalResult:
        """Real RAGAS evaluation (requires OPENAI_API_KEY)."""
        from ragas import evaluate
        from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall
        from datasets import Dataset

        data = Dataset.from_list([{
            "question": sample.question,
            "answer": sample.answer,
            "contexts": sample.contexts,
            "ground_truth": sample.ground_truth,
        }])

        result = evaluate(data, metrics=[faithfulness, answer_relevancy, context_precision, context_recall])
        row = result.to_pandas().iloc[0]

        return EvalResult(
            faithfulness=row["faithfulness"],
            answer_relevancy=row["answer_relevancy"],
            context_precision=row["context_precision"],
            context_recall=row["context_recall"],
        )

    def aggregate(self, results: List[EvalResult]) -> Dict[str, float]:
        """Compute average scores across the evaluation set."""
        if not results:
            return {}

        return {
            "faithfulness":       round(sum(r.faithfulness for r in results) / len(results), 3),
            "answer_relevancy":   round(sum(r.answer_relevancy for r in results) / len(results), 3),
            "context_precision":  round(sum(r.context_precision for r in results) / len(results), 3),
            "context_recall":     round(sum(r.context_recall for r in results) / len(results), 3),
            "overall":            round(sum(r.overall_score for r in results) / len(results), 3),
        }


# ─────────────────────────────────────────────────────────────
# LLM-AS-JUDGE
# ─────────────────────────────────────────────────────────────

class LLMJudge:
    """
    Use a powerful LLM to score another LLM's output.

    CV Reference: "automated LLM evaluation pipeline (LLM-as-judge + RAGAS),
    improving response quality scores by 35%"

    HOW IT WORKS:
      1. You have a question, a generated answer, and a reference answer
      2. You ask GPT-4 to score the generated answer on specific criteria
      3. GPT-4 returns a numeric score + explanation
      4. Aggregate scores across your eval set to track quality over time

    WHY USE IT?
      - Scales to thousands of samples (vs expensive human evaluation)
      - Consistent scoring (humans disagree; GPT-4 is deterministic at temp=0)
      - Can evaluate nuanced qualities: tone, completeness, safety
    """

    JUDGE_PROMPT = """You are an expert evaluator assessing the quality of AI-generated answers.

Given:
- QUESTION: {question}
- REFERENCE ANSWER: {reference}
- GENERATED ANSWER: {answer}

Score the GENERATED ANSWER on these criteria (each 1-5):
1. Correctness: Is it factually accurate vs the reference?
2. Completeness: Does it cover all key points from the reference?
3. Clarity: Is it well-written and easy to understand?
4. Conciseness: Does it avoid unnecessary verbosity?

Return ONLY valid JSON in this exact format:
{{
  "correctness": <int 1-5>,
  "completeness": <int 1-5>,
  "clarity": <int 1-5>,
  "conciseness": <int 1-5>,
  "reasoning": "<one sentence explaining the scores>"
}}"""

    def __init__(self, simulate: bool = True):
        self.simulate = simulate

    def judge(self, question: str, reference: str, generated: str) -> Dict[str, Any]:
        """Score one generated answer."""
        if self.simulate:
            return self._simulated_judge(question, reference, generated)

        return self._llm_judge(question, reference, generated)

    def _simulated_judge(self, question: str, reference: str, generated: str) -> Dict[str, Any]:
        """Simulate GPT-4 judging without an API call."""
        import random
        random.seed(hash(question) % 1000)  # Deterministic for demo

        scores = {
            "correctness":  random.randint(3, 5),
            "completeness": random.randint(3, 5),
            "clarity":      random.randint(3, 5),
            "conciseness":  random.randint(3, 5),
            "reasoning":    "SIMULATED: set simulate=False with OPENAI_API_KEY for real evaluation",
        }
        scores["overall"] = sum(v for k, v in scores.items() if isinstance(v, int)) / 4
        return scores

    def _llm_judge(self, question: str, reference: str, generated: str) -> Dict[str, Any]:
        """Real GPT-4 judge evaluation."""
        from openai import OpenAI
        client = OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

        prompt = self.JUDGE_PROMPT.format(
            question=question,
            reference=reference,
            answer=generated,
        )

        response = client.chat.completions.create(
            model="gpt-4o",
            messages=[{"role": "user", "content": prompt}],
            temperature=0,   # Deterministic for consistent scoring
            max_tokens=300,
        )

        raw = response.choices[0].message.content.strip()
        result = json.loads(raw)
        result["overall"] = sum(v for k, v in result.items() if isinstance(v, int)) / 4
        return result


